-- recipe_ingredients — a row is EITHER a raw-material line OR a sub-recipe line.
create table if not exists public.recipe_ingredients (
  id               uuid          primary key default gen_random_uuid(),
  recipe_id        uuid          not null references public.recipes (id) on delete cascade,
  raw_material_id  uuid          references public.raw_materials (id) on delete restrict,
  sub_recipe_id    uuid          references public.recipes (id) on delete restrict,
  quantity_needed  numeric(14,4) not null check (quantity_needed > 0),
  location_id      uuid          not null references public.locations (id) on delete cascade,
  -- exactly one of {raw_material_id, sub_recipe_id} is set
  constraint recipe_ingredients_kind_chk check (num_nonnulls(raw_material_id, sub_recipe_id) = 1),
  -- a recipe may not be its own sub-recipe (deeper cycles guarded in app + recipe_cogs)
  constraint recipe_ingredients_no_self_subrecipe_chk
    check (sub_recipe_id is null or sub_recipe_id <> recipe_id),
  -- non-partial unique → valid ON CONFLICT arbiter for the sheet-pull upsert.
  -- NULL raw_material_id (sub-recipe lines) are DISTINCT, so they never collide.
  constraint recipe_ingredients_recipe_material_key unique (recipe_id, raw_material_id)
);
create index if not exists idx_recipe_ingredients_recipe on public.recipe_ingredients (recipe_id);
create index if not exists idx_recipe_ingredients_sub_recipe on public.recipe_ingredients (sub_recipe_id);
-- one sub-recipe line per (recipe, sub-recipe)
create unique index if not exists uq_recipe_ingredients_subrecipe
  on public.recipe_ingredients (recipe_id, sub_recipe_id) where sub_recipe_id is not null;

-- inventory_ledger — immutable. Convention: credits to_department, debits from_department.
create table if not exists public.inventory_ledger (
  id                  uuid          primary key default gen_random_uuid(),
  raw_material_id     uuid          not null references public.raw_materials (id) on delete restrict,
  from_department_id  int           references public.departments (id) on delete restrict,
  to_department_id    int           references public.departments (id) on delete restrict,
  vendor_id           uuid          references public.vendors (id) on delete restrict,
  type                varchar(40)   not null
                        constraint inventory_ledger_type_check check (type in (
                          'PURCHASE',
                          'ISSUE_TO_KITCHEN',
                          'INTER_DEPARTMENT_TRANSFER',
                          'MANUAL_SALE',
                          'WASTAGE',
                          'VARIANCE_RECONCILIATION',
                          'SALES_DEPLETION'
                        )),
  quantity            numeric(14,4) not null,
  unit_price          numeric(14,4),
  wastage_reason      varchar(255),
  created_by          uuid          references public.profiles (id) on delete set null,
  source_ref          text,         -- POS order id for SALES_DEPLETION idempotency
