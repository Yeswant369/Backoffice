-- SECTION 4: TENANT HELPER FUNCTIONS (SECURITY DEFINER → no RLS recursion)
-- =============================================================================
create or replace function public.is_admin()
returns boolean language sql security definer set search_path = public stable as $$
  select exists (select 1 from public.profiles where id = auth.uid() and 1 = any (roles));
$$;

create or replace function public.current_location_id()
returns uuid language sql security definer set search_path = public stable as $$
  select location_id from public.profiles where id = auth.uid();
$$;

create or replace function public.current_org_id()
returns uuid language sql security definer set search_path = public stable as $$
  select organization_id from public.locations
  where id = (select location_id from public.profiles where id = auth.uid());
$$;

create or replace function public.set_location_id()
returns trigger language plpgsql security definer set search_path = public as $$
begin
  if new.location_id is null then
    new.location_id := public.current_location_id();
  end if;
  return new;
end;
$$;

grant execute on function public.is_admin() to authenticated;
grant execute on function public.current_location_id() to authenticated;
grant execute on function public.current_org_id() to authenticated;

-- Cross-outlet roles (5 = Area Manager, 6 = Owner) and read/write scope helpers.
create or replace function public.is_owner()
returns boolean language sql security definer set search_path = public stable as $$
  select exists (select 1 from public.profiles where id = auth.uid() and 6 = any (roles));
$$;

create or replace function public.can_manage_org()
returns boolean language sql security definer set search_path = public stable as $$
  select exists (
    select 1 from public.profiles
     where id = auth.uid() and (1 = any (roles) or 6 = any (roles))
  );
$$;

-- READ scope: Owner → whole org; Area Manager → home + assigned outlets;
-- everyone else (Admin/Manager/Store/Kitchen) → home only (unchanged behavior).
create or replace function public.current_location_ids()
returns setof uuid language plpgsql security definer set search_path = public stable as $$
declare
  v_uid   uuid := auth.uid();
  v_roles int[];
  v_home  uuid;
  v_org   uuid;
begin
  select roles, location_id into v_roles, v_home
    from public.profiles where id = v_uid;
  if v_roles is null then
    return;
  end if;
  if 6 = any (v_roles) then
    select organization_id into v_org from public.locations where id = v_home;
    return query select id from public.locations where organization_id = v_org;
    return;
  end if;
  if 5 = any (v_roles) then
    return query
      select v_home
      union
      select pl.location_id from public.profile_locations pl where pl.profile_id = v_uid;
    return;
  end if;
  return query select v_home;
end;
$$;

-- WRITE scope: home only — cross-outlet access is READ-ONLY.
create or replace function public.current_writable_location_ids()
returns setof uuid language sql security definer set search_path = public stable as $$
  select public.current_location_id();
$$;

grant execute on function public.is_owner()                       to authenticated;
grant execute on function public.can_manage_org()                 to authenticated;
grant execute on function public.current_location_ids()           to authenticated;

-- set_pos_config — admin-only writer for the outlet's Petpooja restID +
-- per-platform commissions (avoids a broad UPDATE grant on locations).
create or replace function public.set_pos_config(p_rest_id text, p_commissions jsonb)
returns void language plpgsql security definer set search_path = public as $$
declare home uuid;
begin
  if not exists (
    select 1 from public.profiles
     where id = auth.uid() and (1 = any(roles) or 6 = any(roles))
  ) then
    raise exception 'Only administrators can change POS configuration';
  end if;
  home := public.current_location_id();
  if home is null then raise exception 'No home location for this account'; end if;
  update public.locations
     set petpooja_rest_id = nullif(btrim(p_rest_id), ''),
         pos_commissions  = coalesce(p_commissions, '{}'::jsonb)
   where id = home;
end $$;
revoke all on function public.set_pos_config(text, jsonb) from public;
grant execute on function public.set_pos_config(text, jsonb) to authenticated;

-- Per-outlet Petpooja creds vault. Secret columns (no authenticated grant);
-- written via this admin-only setter (blank field keeps the stored value), and
-- the UI can read only a boolean "configured" status, never the values.
create or replace function public.set_pos_creds(
  p_app_key text, p_app_secret text, p_access_token text)
returns void language plpgsql security definer set search_path = public as $$
declare home uuid;
begin
  if not exists (
    select 1 from public.profiles
     where id = auth.uid() and (1 = any(roles) or 6 = any(roles))
  ) then
    raise exception 'Only administrators can change POS credentials';
  end if;
  home := public.current_location_id();
  if home is null then raise exception 'No home location for this account'; end if;
  update public.locations set
    petpooja_app_key      = coalesce(nullif(btrim(p_app_key), ''),      petpooja_app_key),
    petpooja_app_secret   = coalesce(nullif(btrim(p_app_secret), ''),   petpooja_app_secret),
    petpooja_access_token = coalesce(nullif(btrim(p_access_token), ''), petpooja_access_token)
  where id = home;
end $$;
