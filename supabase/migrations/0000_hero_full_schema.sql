-- Auto-provision a profile on signup (only once the tenant location is known).
create or replace function public.handle_new_user()
returns trigger language plpgsql security definer set search_path = public as $$
declare
  v_roles    int[];
  v_location uuid;
begin
  begin
    select array(select jsonb_array_elements_text(new.raw_user_meta_data -> 'roles')::int)
      into v_roles;
  exception when others then v_roles := null;
  end;
  -- Clamp to operational roles 1-4: cross-outlet roles 5/6 are NEVER granted at
  -- signup (service-role-only). user_metadata is client-writable — a self-signup
  -- must not be able to mint an Owner/Area Manager.
  v_roles := array(select r from unnest(coalesce(v_roles, '{}'::int[])) r where r between 1 and 4);
  if array_length(v_roles, 1) is null then
    v_roles := array[3]::int[];
  end if;

  begin
    v_location := (new.raw_user_meta_data ->> 'location_id')::uuid;
  exception when others then v_location := null;
  end;

  if v_location is not null then
    insert into public.profiles (id, full_name, roles, location_id)
    values (new.id, new.raw_user_meta_data ->> 'full_name', v_roles, v_location)
    on conflict (id) do nothing;
  end if;
  return new;
end;
$$;

-- Explode a manual sale's recipe into MANUAL_SALE ledger deductions from Kitchen.
create or replace function public.handle_manual_sale()
returns trigger language plpgsql security definer set search_path = public as $$
declare
  v_kitchen_id int;
begin
  select id into v_kitchen_id from public.departments
  where lower(name) = 'kitchen' and location_id = new.location_id limit 1;

  if v_kitchen_id is null then
    raise exception
      'No "Kitchen" department for location % — cannot explode manual sale.', new.location_id
      using errcode = 'no_data_found';
  end if;

  insert into public.inventory_ledger
    (raw_material_id, from_department_id, to_department_id, type, quantity, location_id, transaction_date)
  select ri.raw_material_id, v_kitchen_id, null, 'MANUAL_SALE',
         ri.quantity_needed * new.quantity_sold, new.location_id, new.sale_date
  from public.recipe_ingredients ri
  where ri.recipe_id = new.recipe_id and ri.raw_material_id is not null;
  return new;
end;
$$;

-- Hard-block UPDATE/DELETE on append-only tables.
create or replace function public.prevent_mutation()
returns trigger language plpgsql as $$
begin
  raise exception
    'Table % is append-only: % is not permitted. Post a reversing row instead.',
    tg_table_name, tg_op using errcode = 'restrict_violation';
  return null;
end;
$$;

-- Backstop the security gate: block roles/location_id self-edits unless the
-- caller is a trusted backend (service role → no auth.uid()) or an admin.
create or replace function public.guard_profile_privileges()
returns trigger language plpgsql set search_path = public as $$
begin
  if (new.roles is distinct from old.roles)
     or (new.location_id is distinct from old.location_id) then
    if auth.uid() is not null and not public.is_admin() then
      raise exception 'Not authorized to modify profile roles or location_id.'
        using errcode = 'insufficient_privilege';
    end if;
  end if;
  return new;
end;
$$;

