-- =============================================================================
-- SECTION 12: CROSS-CLIENT SHEET-SYNC LOCK (coalescing distributed lock)
-- Serializes /api/sheets/push per location so concurrent clients can't race on
-- the same spreadsheet. Session advisory locks are unreliable on Supabase's
-- pooled connections, so we use a lock table + SECURITY DEFINER functions. Both
-- functions derive the location from current_location_id() (caller's HOME), so a
-- caller can only ever lock their own outlet. 120s stale timeout = no deadlock.
-- =============================================================================

create table if not exists public.sheet_sync_locks (
  location_id uuid primary key references public.locations(id) on delete cascade,
  locked_at   timestamptz,
  locked_by   uuid,
  token       uuid,
  pending     boolean not null default false
);
alter table public.sheet_sync_locks add column if not exists token uuid;

alter table public.sheet_sync_locks enable row level security;
-- No policies: direct access denied for `authenticated`; all access via the
-- SECURITY DEFINER functions below (which scope to the caller's home location).

drop function if exists public.acquire_sheet_lock();
drop function if exists public.release_sheet_lock();
drop function if exists public.release_sheet_lock(uuid);

-- Returns a fencing TOKEN when acquired; null when a fresh holder has it (sets
-- pending so the holder re-runs) or there is no home location. 300s stale
-- timeout (keep ≥ the platform's max function duration) reclaims a dead holder.
create or replace function public.acquire_sheet_lock()
returns uuid
language plpgsql
security definer
set search_path = public
as $$
declare
  loc uuid := public.current_location_id();
  tok uuid := gen_random_uuid();
  got uuid;
begin
  if loc is null then
    return null;
  end if;

  insert into public.sheet_sync_locks (location_id, locked_at, locked_by, token, pending)
  values (loc, now(), auth.uid(), tok, false)
  on conflict (location_id) do update
    set locked_at = now(),
        locked_by = auth.uid(),
        token     = tok,
        pending   = false
    where sheet_sync_locks.locked_at is null
       or sheet_sync_locks.locked_at < now() - interval '300 seconds'
  returning token into got;

  if got is not null then
    return got; -- = tok; we hold the lock with this fencing token
  end if;

  update public.sheet_sync_locks set pending = true where location_id = loc;
  return null;
end;
$$;

-- Release the lock IF the fencing token still matches (we still own it). Returns
-- whether a re-run is needed (a save coalesced onto this run). A no-op (false) if
-- we were taken over as stale — so we never clear another client's lock.
create or replace function public.release_sheet_lock(tok uuid)
returns boolean
language plpgsql
security definer
set search_path = public
as $$
declare
  loc uuid := public.current_location_id();
  was_pending boolean;
begin
  if loc is null then
    return false;
  end if;

  select pending into was_pending
    from public.sheet_sync_locks
   where location_id = loc and token = tok
   for update;

  if not found then
    return false; -- taken over (token changed) — do not touch the new holder
  end if;

  update public.sheet_sync_locks
     set locked_at = null, token = null, pending = false
   where location_id = loc and token = tok;

  return coalesce(was_pending, false);
end;
$$;

grant execute on function public.acquire_sheet_lock() to authenticated;
grant execute on function public.release_sheet_lock(uuid) to authenticated;

