  location_id         uuid          not null references public.locations (id) on delete cascade,
  created_at          timestamptz   not null default now(),
  -- NULLs are DISTINCT, so legacy/non-POS rows never collide; two SALES_DEPLETION
  -- rows for the same (location, order, material) cannot both exist (retry-safe).
  constraint uq_inventory_ledger_source_ref unique (location_id, source_ref, raw_material_id)
);
create index if not exists idx_ledger_raw_material on public.inventory_ledger (raw_material_id);
create index if not exists idx_ledger_type         on public.inventory_ledger (type);
create index if not exists idx_ledger_from_dept     on public.inventory_ledger (from_department_id);
create index if not exists idx_ledger_to_dept       on public.inventory_ledger (to_department_id);
