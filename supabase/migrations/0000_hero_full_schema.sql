-- =============================================================================
-- BOH ERP — HERO SCHEMA (consolidated 0001 → 0013, final state)
--
-- A single, idempotent, dependency-ordered script that builds the ENTIRE
-- database from an empty Supabase project. Every later fix is already baked in:
--   * is_admin() / current_location_id() are SECURITY DEFINER (no RLS recursion)
--   * location_id is NOT NULL on every operational table from the start
--   * recipe_ingredients supports SUB-RECIPES (XOR raw_material/sub_recipe)
--   * inventory_ledger has SALES_DEPLETION + source_ref idempotency
--   * pos_sales / unmapped_sales carry (order_id, line_no) idempotency keys
--   * recipe_cogs() compounds sub-recipe overhead; recipe_costing uses LATERAL
--
-- Philosophy: transactional ledger. Balances are NEVER stored — they are derived
-- through VIEWS. inventory_ledger / vendor_payments are APPEND-ONLY (enforced by
-- the prevent_mutation trigger). Multi-tenant: every row is scoped to location_id
-- and RLS isolates tenants via current_location_id().
--
-- HOW TO RUN: paste into the Supabase SQL Editor on a CLEAN database and run once.
-- Safe to re-run (idempotent). Edit the v_admin_email in SECTION 12 first.
-- =============================================================================

create extension if not exists pgcrypto;

-- =============================================================================
-- SECTION 1: TENANCY — organizations → locations
-- =============================================================================
create table if not exists public.organizations (
  id          uuid        primary key default gen_random_uuid(),
  name        varchar(255) not null,
  created_at  timestamptz not null default now()
);

create table if not exists public.locations (
  id                    uuid        primary key default gen_random_uuid(),
  organization_id       uuid        not null references public.organizations (id) on delete cascade,
  name                  varchar(255) not null,
  google_spreadsheet_id text,
  pos_webhook_secret    text,            -- per-tenant Petpooja webhook secret
  petpooja_rest_id      text,            -- Petpooja Get-Orders mapping code (per outlet)
  pos_commissions       jsonb       not null default '{}'::jsonb,  -- {"Swiggy":25,"Zomato":22}
  pos_last_synced_at    timestamptz,
  petpooja_app_key      text,            -- per-outlet Petpooja creds (SECRET — no authenticated grant)
  petpooja_app_secret   text,
  petpooja_access_token text,
  created_at            timestamptz not null default now()
);
create index if not exists idx_locations_org on public.locations (organization_id);

