-- live_stock — net on-hand per (location, material, department).
create or replace view public.live_stock with (security_invoker = on) as
with movements as (
  select location_id, raw_material_id, to_department_id   as department_id,  quantity as qty
    from public.inventory_ledger where to_department_id is not null
  union all
  select location_id, raw_material_id, from_department_id as department_id, -quantity as qty
    from public.inventory_ledger where from_department_id is not null
)
select
  rm.location_id, rm.id as raw_material_id, rm.name as raw_material_name,
  rm.category, rm.stock_unit, rm.par_level, m.department_id,
  d.name as department_name,
  coalesce(sum(m.qty), 0) as current_stock,
  (coalesce(sum(m.qty), 0) < rm.par_level) as below_par
from public.raw_materials rm
join movements m          on m.raw_material_id = rm.id and m.location_id = rm.location_id
join public.departments d on d.id = m.department_id and d.location_id = m.location_id
group by rm.location_id, rm.id, rm.name, rm.category, rm.stock_unit, rm.par_level,
         m.department_id, d.name;

-- vendor_dues — purchases (ledger) minus payments, per vendor.
create or replace view public.vendor_dues with (security_invoker = on) as
with purchases as (
  select location_id, vendor_id,
         coalesce(sum(quantity * coalesce(unit_price, 0)), 0) as total_purchased
    from public.inventory_ledger
   where type = 'PURCHASE' and vendor_id is not null
   group by location_id, vendor_id
),
payments as (
  select location_id, vendor_id, coalesce(sum(amount_paid), 0) as total_paid
    from public.vendor_payments group by location_id, vendor_id
)
select
  v.location_id, v.id as vendor_id, v.vendor_code, v.name as vendor_name, v.status,
  coalesce(p.total_purchased, 0) as total_purchased,
  coalesce(pay.total_paid, 0)    as total_paid,
  coalesce(p.total_purchased, 0) - coalesce(pay.total_paid, 0) as outstanding_due
from public.vendors v
left join purchases p   on p.vendor_id   = v.id and p.location_id   = v.location_id
left join payments  pay on pay.vendor_id = v.id and pay.location_id = v.location_id;

-- vendor_master — full vendor row + AUTO stats (paid MTD/YTD, outstanding, last
-- payment/purchase). Nothing hand-typed; powers the Vendor Master sheet + UI.
create or replace view public.vendor_master with (security_invoker = on) as
with pay as (
  select location_id, vendor_id,
         sum(amount_paid) filter (
           where payment_date >= date_trunc('month', (now() at time zone 'Asia/Kolkata'))::date
             and payment_date <= (now() at time zone 'Asia/Kolkata')::date) as paid_mtd,
         sum(amount_paid) filter (
           where payment_date >= date_trunc('year', (now() at time zone 'Asia/Kolkata'))::date
             and payment_date <= (now() at time zone 'Asia/Kolkata')::date) as paid_ytd,
         max(payment_date) as last_payment
    from public.vendor_payments
   group by location_id, vendor_id
),
purch as (
  select location_id, vendor_id,
         max(coalesce(transaction_date::date, (created_at at time zone 'Asia/Kolkata')::date)) as last_purchase
    from public.inventory_ledger
   where type = 'PURCHASE' and vendor_id is not null
   group by location_id, vendor_id
)
select
  v.id, v.location_id, v.vendor_code, v.name, v.nature_of_supply, v.contact_person,
  v.phone, v.alt_phone, v.email, v.category,
  v.bank_name, v.account_number, v.ifsc_code, v.upi_id, v.payment_terms,
  v.address, v.gstin, v.notes,
  coalesce(pay.paid_mtd, 0)       as total_paid_mtd,
  coalesce(pay.paid_ytd, 0)       as total_paid_ytd,
  coalesce(vd.outstanding_due, 0) as outstanding,
  pay.last_payment,
  pu.last_purchase,
  v.created_at                    as first_added,
  v.status,
  v.dormancy_note
from public.vendors v
left join pay   on pay.vendor_id = v.id and pay.location_id = v.location_id
left join purch pu on pu.vendor_id = v.id and pu.location_id = v.location_id
left join public.vendor_dues vd on vd.vendor_id = v.id and vd.location_id = v.location_id;

-- dues_tracker — auto outstanding / status / days-pending for the Dues Tracker.
create or replace view public.dues_tracker with (security_invoker = on) as
select
  id, location_id,
  created_at::date as date_created,
  person_name, amount, reason, linked_date, date_settled,
  settled_amount, settled_mode, notes,
  greatest(amount - settled_amount, 0) as outstanding,
  case when settled_amount >= amount then 'SETTLED'
       when settled_amount > 0       then 'PARTIAL'
       else 'PENDING' end as status,
  case when settled_amount >= amount then null
       else ((now() at time zone 'Asia/Kolkata')::date - coalesce(linked_date, created_at::date)) end as days_pending
from public.dues;

-- weighted_average_cost — total purchase spend / total purchased qty, per material.
create or replace view public.weighted_average_cost with (security_invoker = on) as
select
  rm.location_id, rm.id as raw_material_id, rm.name as raw_material_name, rm.stock_unit,
  coalesce(sum(il.quantity), 0) as total_quantity_purchased,
  coalesce(sum(il.quantity * coalesce(il.unit_price, 0)), 0) as total_spend,
  case when coalesce(sum(il.quantity), 0) > 0
       then sum(il.quantity * coalesce(il.unit_price, 0)) / sum(il.quantity)
       else 0 end as weighted_avg_cost
from public.raw_materials rm
left join public.inventory_ledger il
       on il.raw_material_id = rm.id and il.location_id = rm.location_id and il.type = 'PURCHASE'
group by rm.location_id, rm.id, rm.name, rm.stock_unit;

-- recipe_cogs() — recursive per-portion COGS valued at WAC. Created here, AFTER
-- weighted_average_cost, because its SQL body is validated against that view now.
-- Cycle-safe (path[] guard); compounds each sub-recipe's overhead.
create or replace function public.recipe_cogs(p_recipe_id uuid)
returns numeric language sql stable security invoker set search_path = public as $$
  with recursive exploded as (
    select ri.recipe_id, ri.raw_material_id, ri.sub_recipe_id, ri.quantity_needed,
           array[ri.recipe_id] as path, 1::numeric as multiplier
    from public.recipe_ingredients ri
    where ri.recipe_id = p_recipe_id
    union all
    select child.recipe_id, child.raw_material_id, child.sub_recipe_id, child.quantity_needed,
           e.path || child.recipe_id,
           e.multiplier * e.quantity_needed
             / greatest(coalesce(sub.yield_portions, 1), 1)
             * (1 + coalesce(sub.overhead_percentage, 0) / 100.0) as multiplier
    from exploded e
    join public.recipes sub              on sub.id = e.sub_recipe_id
    join public.recipe_ingredients child on child.recipe_id = e.sub_recipe_id
    where e.sub_recipe_id is not null
      and not (child.recipe_id = any (e.path))
  ),
  batch as (
    select coalesce(sum(e.multiplier * e.quantity_needed
                        * coalesce(wac.weighted_avg_cost, 0)), 0) as batch_cost
    from exploded e
    join public.raw_materials rm on rm.id = e.raw_material_id
    left join public.weighted_average_cost wac
      on wac.raw_material_id = e.raw_material_id and wac.location_id = rm.location_id
    where e.raw_material_id is not null
  )
  select round((b.batch_cost / greatest(r.yield_portions, 1))
               * (1 + coalesce(r.overhead_percentage, 0) / 100.0), 4)
  from public.recipes r cross join batch b
  where r.id = p_recipe_id;
$$;
grant execute on function public.recipe_cogs(uuid) to authenticated;

-- theoretical_stock — intention-revealing alias over live_stock (the book on-hand).
create or replace view public.theoretical_stock with (security_invoker = on) as
select location_id, raw_material_id, raw_material_name, category, stock_unit, par_level,
       department_id, department_name, current_stock as theoretical_qty, below_par
from public.live_stock;

-- stock_count_variance — each manager count paired with its frozen system snapshot.
create or replace view public.stock_count_variance with (security_invoker = on) as
select
  sc.location_id, sc.count_date, sc.department_id, d.name as department_name,
  sc.raw_material_id, rm.name as raw_material_name, rm.stock_unit,
  sc.system_qty, sc.actual_qty, sc.variance, sc.par_level,
  coalesce(wac.weighted_avg_cost, 0) as unit_cost,
  round(sc.variance * coalesce(wac.weighted_avg_cost, 0), 2) as variance_value,
  (sc.actual_qty < sc.par_level) as below_par
from public.stock_counts sc
join public.raw_materials rm on rm.id = sc.raw_material_id and rm.location_id = sc.location_id
join public.departments d    on d.id = sc.department_id and d.location_id = sc.location_id
left join public.weighted_average_cost wac
  on wac.raw_material_id = sc.raw_material_id and wac.location_id = sc.location_id;

-- recipe_costing — selling price, COGS, margin per recipe (recipe_cogs once via LATERAL).
create or replace view public.recipe_costing with (security_invoker = on) as
select
  r.id as recipe_id, r.location_id, r.name as recipe_name, r.category, r.selling_price,
  c.cogs,
  (r.selling_price - c.cogs) as margin_value,
  case when r.selling_price > 0
       then round((r.selling_price - c.cogs) / r.selling_price * 100.0, 2) else 0 end as margin_pct,
  case when r.selling_price > 0
       then round(c.cogs / r.selling_price * 100.0, 2) else 0 end as food_cost_pct
from public.recipes r
cross join lateral (select public.recipe_cogs(r.id) as cogs) c;

-- recipe_sales_volume — portions sold per recipe per day (pos_sales + manual sales).
create or replace view public.recipe_sales_volume with (security_invoker = on) as
select s.location_id, s.recipe_id, s.sold_on, sum(s.quantity)::bigint as portions
from (
  select location_id, recipe_id, (sold_at at time zone 'Asia/Kolkata')::date as sold_on, quantity
    from public.pos_sales
  union all
  select location_id, recipe_id, sale_date as sold_on, quantity_sold as quantity
    from public.manual_sales_log
) s
group by s.location_id, s.recipe_id, s.sold_on;

-- reorder_suggestions — below-PAR materials with suggested order qty, est cost,
-- usual vendor and days-of-cover from trailing-14d SALES_DEPLETION velocity.
create or replace view public.reorder_suggestions with (security_invoker = on) as
with on_hand as (
  select location_id, raw_material_id, sum(current_stock) as current_stock
    from public.live_stock group by location_id, raw_material_id
),
velocity as (
  select location_id, raw_material_id, coalesce(sum(quantity), 0) / 14.0 as daily_usage
    from public.inventory_ledger
   where type = 'SALES_DEPLETION' and created_at >= now() - interval '14 days'
   group by location_id, raw_material_id
)
select
  rm.location_id, rm.id as raw_material_id, rm.name as raw_material_name, rm.stock_unit, rm.par_level,
  coalesce(oh.current_stock, 0) as current_stock,
  greatest(rm.par_level - coalesce(oh.current_stock, 0), 0) as suggested_qty,
  coalesce(wac.weighted_avg_cost, 0) as last_unit_cost,
  round(greatest(rm.par_level - coalesce(oh.current_stock, 0), 0) * coalesce(wac.weighted_avg_cost, 0), 2) as est_cost,
  rm.vendor_id, v.name as vendor_name,
  coalesce(vel.daily_usage, 0) as daily_usage,
  case when coalesce(vel.daily_usage, 0) > 0
       then round(coalesce(oh.current_stock, 0) / vel.daily_usage, 1) else null end as days_cover
from public.raw_materials rm
left join on_hand  oh  on oh.raw_material_id  = rm.id and oh.location_id  = rm.location_id
left join velocity vel on vel.raw_material_id = rm.id and vel.location_id = rm.location_id
left join public.weighted_average_cost wac on wac.raw_material_id = rm.id and wac.location_id = rm.location_id
left join public.vendors v on v.id = rm.vendor_id and v.location_id = rm.location_id
where coalesce(oh.current_stock, 0) < rm.par_level;

-- pos_revenue_expected — expected gross per day (Σ pos_sales × selling_price)
-- to cross-check the manager's hand-entered reconciliation.
create or replace view public.pos_revenue_expected with (security_invoker = on) as
select
  ps.location_id,
  (ps.sold_at at time zone 'Asia/Kolkata')::date as sale_date,
  sum(ps.quantity * r.selling_price) as expected_gross,
  sum(ps.quantity) as items_sold
from public.pos_sales ps
join public.recipes r on r.id = ps.recipe_id and r.location_id = ps.location_id
group by ps.location_id, (ps.sold_at at time zone 'Asia/Kolkata')::date;

-- kitchen_production_view — prepared/sold/wasted + variance + item cost (needs
-- recipe_cogs + weighted_average_cost, defined above).
create or replace view public.kitchen_production_view with (security_invoker = on) as
select
  kp.id, kp.location_id, kp.production_date,
  kp.department_id, d.name as department_name,
  kp.recipe_id, r.name as recipe_name,
  kp.prepared_qty,
  coalesce(rsv.portions, kp.sold_qty)::numeric(14,3) as sold_qty,
  kp.wastage_qty,
  (kp.prepared_qty - coalesce(rsv.portions, kp.sold_qty) - kp.wastage_qty) as variance,
  r.selling_price,
  round(public.recipe_cogs(kp.recipe_id), 2)                 as unit_cost,
  round(kp.wastage_qty * public.recipe_cogs(kp.recipe_id), 2) as wastage_cost,
  kp.notes
from public.kitchen_production kp
join public.recipes r on r.id = kp.recipe_id
left join public.departments d on d.id = kp.department_id and d.location_id = kp.location_id
left join public.recipe_sales_volume rsv
  on rsv.recipe_id = kp.recipe_id and rsv.location_id = kp.location_id
 and rsv.sold_on = kp.production_date
 -- attach sales only to the row in the recipe's OWN department, so a recipe
 -- prepared in two departments never has the full sales broadcast to both.
 and kp.department_id = r.department_id;

-- department_sales — sales (manual + POS) rolled to the item's department.
create or replace view public.department_sales with (security_invoker = on) as
select location_id, department_id, sold_on,
       sum(qty)        as qty_sold,
       sum(sale_value) as sale_value
from (
  select m.location_id, r.department_id, m.sale_date as sold_on,
         m.quantity_sold as qty, m.quantity_sold * r.selling_price as sale_value
    from public.manual_sales_log m
    join public.recipes r on r.id = m.recipe_id and r.location_id = m.location_id
  union all
  select p.location_id, r.department_id,
         (p.sold_at at time zone 'Asia/Kolkata')::date as sold_on,
         p.quantity as qty, p.quantity * r.selling_price as sale_value
    from public.pos_sales p
    join public.recipes r on r.id = p.recipe_id and r.location_id = p.location_id
) s
group by location_id, department_id, sold_on;

-- department_pl — per department: raw issued-in cost (net of inter-dept
-- transfers out), sale revenue, item wastage. Keeps the NULL/Unassigned bucket.
create or replace view public.department_pl with (security_invoker = on) as
with issued as (
  select location_id, department_id, sum(amt) as issued_cost
  from (
    select il.location_id, il.to_department_id as department_id,
           il.quantity * coalesce(wac.weighted_avg_cost, 0) as amt
      from public.inventory_ledger il
      left join public.weighted_average_cost wac
        on wac.raw_material_id = il.raw_material_id and wac.location_id = il.location_id
     where il.type in ('ISSUE_TO_KITCHEN', 'INTER_DEPARTMENT_TRANSFER')
       and il.to_department_id is not null
    union all
    select il.location_id, il.from_department_id as department_id,
           - il.quantity * coalesce(wac.weighted_avg_cost, 0) as amt
      from public.inventory_ledger il
      left join public.weighted_average_cost wac
        on wac.raw_material_id = il.raw_material_id and wac.location_id = il.location_id
     where il.type = 'INTER_DEPARTMENT_TRANSFER' and il.from_department_id is not null
  ) x
  group by location_id, department_id
),
sold as (
  select location_id, department_id,
         sum(sale_value) as sale_value, sum(qty_sold) as items_sold
    from public.department_sales
   group by location_id, department_id
),
waste as (
  select location_id, department_id, sum(wastage_cost) as item_wastage_cost
    from public.kitchen_production_view
   group by location_id, department_id
),
keys as (
  select location_id, department_id from issued
  union select location_id, department_id from sold
  union select location_id, department_id from waste
  union select location_id, id as department_id from public.departments
)
select
  k.location_id, k.department_id, d.name as department_name,
  coalesce(i.issued_cost, 0)       as issued_cost,
  coalesce(s.sale_value, 0)        as sale_value,
  coalesce(s.items_sold, 0)        as items_sold,
  coalesce(w.item_wastage_cost, 0) as item_wastage_cost
from keys k
left join public.departments d on d.id = k.department_id and d.location_id = k.location_id
left join issued i on i.location_id = k.location_id and i.department_id is not distinct from k.department_id
left join sold   s on s.location_id = k.location_id and s.department_id is not distinct from k.department_id
left join waste  w on w.location_id = k.location_id and w.department_id is not distinct from k.department_id;

-- pl_daily — theoretical vs actual COGS + profit per location per day.
create or replace view public.pl_daily with (security_invoker = on) as
with cogs as (
  select
    il.location_id,
    -- business date: MANUAL_SALE carries the sale_date in transaction_date so its
    -- cost lands on the same day as its revenue (backdated sales reconcile).
    coalesce(il.transaction_date, (il.created_at at time zone 'Asia/Kolkata')::date) as d,
    sum(case when il.type in ('SALES_DEPLETION', 'MANUAL_SALE')
             then il.quantity * coalesce(wac.weighted_avg_cost, 0) else 0 end) as theoretical_cogs,
    sum(case when il.type = 'WASTAGE'
             then il.quantity * coalesce(wac.weighted_avg_cost, 0) else 0 end) as wastage_cost,
    sum(case when il.type = 'VARIANCE_RECONCILIATION' and il.from_department_id is not null
             then il.quantity * coalesce(wac.weighted_avg_cost, 0)
             when il.type = 'VARIANCE_RECONCILIATION' and il.to_department_id is not null
             then - il.quantity * coalesce(wac.weighted_avg_cost, 0)
             else 0 end) as variance_cost
  from public.inventory_ledger il
  left join public.weighted_average_cost wac
    on wac.raw_material_id = il.raw_material_id and wac.location_id = il.location_id
  where il.type in ('SALES_DEPLETION', 'MANUAL_SALE', 'WASTAGE', 'VARIANCE_RECONCILIATION')
  group by il.location_id, coalesce(il.transaction_date, (il.created_at at time zone 'Asia/Kolkata')::date)
),
rev as (
  select location_id, sold_on as d,
         sum(sale_value) as revenue, sum(qty_sold) as items_sold
    from public.department_sales
   group by location_id, sold_on
)
select
  coalesce(c.location_id, r.location_id) as location_id,
  coalesce(c.d, r.d)                     as pl_date,
  coalesce(r.revenue, 0)                 as revenue,
  coalesce(r.items_sold, 0)              as items_sold,
  coalesce(c.theoretical_cogs, 0)        as theoretical_cogs,
  coalesce(c.wastage_cost, 0)            as wastage_cost,
  coalesce(c.variance_cost, 0)           as variance_cost,
  coalesce(c.theoretical_cogs, 0) + coalesce(c.wastage_cost, 0)
    + coalesce(c.variance_cost, 0)       as actual_cogs
from cogs c
full join rev r on c.location_id = r.location_id and c.d = r.d;

-- POS analytics (Petpooja pull). security_invoker → inherit pos_orders RLS.
create or replace view public.pos_daily_sales with (security_invoker = on) as
select location_id, order_date,
       count(*) as orders, sum(gross_amount) as gross, sum(discount_amount) as discount,
       sum(tax_amount) as gst, sum(net_amount) as net
  from public.pos_orders where status = 'Success'
 group by location_id, order_date;

create or replace view public.pos_sales_by_channel with (security_invoker = on) as
select o.location_id, o.order_date,
       coalesce(nullif(o.channel, ''), o.order_type, 'Unknown') as channel,
       count(*) as orders, sum(o.gross_amount) as gross, sum(o.net_amount) as net,
       sum(o.tax_amount) as gst,
       round(sum(o.net_amount *
         (1 - coalesce((l.pos_commissions ->>
              coalesce(nullif(o.channel, ''), o.order_type, 'Unknown'))::numeric, 0) / 100)), 2) as net_payout
  from public.pos_orders o
  join public.locations l on l.id = o.location_id
 where o.status = 'Success'
 group by o.location_id, o.order_date,
          coalesce(nullif(o.channel, ''), o.order_type, 'Unknown'), l.pos_commissions;

create or replace view public.pos_daypart with (security_invoker = on) as
select location_id, order_date,
       case when h < 12 then 'Morning' when h < 17 then 'Afternoon'
            when h < 22 then 'Evening' else 'Night' end as daypart,
       count(*) as orders, sum(net_amount) as net
  from (select location_id, order_date, net_amount,
               extract(hour from (sold_at at time zone 'Asia/Kolkata'))::int as h
          from public.pos_orders where status = 'Success' and sold_at is not null) t
 group by location_id, order_date, daypart;

create or replace view public.pos_item_report with (security_invoker = on) as
select ps.location_id, po.order_date, ps.recipe_id, r.name as item_name, r.category,
       sum(ps.quantity) as qty_sold,
       round(sum(ps.quantity) * r.selling_price, 2) as revenue,
       round(sum(ps.quantity) * public.recipe_cogs(ps.recipe_id), 2) as food_cost,
       round(sum(ps.quantity) * (r.selling_price - public.recipe_cogs(ps.recipe_id)), 2) as gross_profit
  from public.pos_sales ps
  join public.pos_orders po on po.location_id = ps.location_id and po.order_key = ps.order_id
  join public.recipes r on r.id = ps.recipe_id
 where po.status = 'Success'
 group by ps.location_id, po.order_date, ps.recipe_id, r.name, r.category, r.selling_price;

